// Addition
let myAge = 30;
console.log(myAge);

myAge += 1;
console.log(myAge);

console.log(myAge++);
console.log(++myAge);

// Subtraction
myAge -= 2;
myAge--;
--myAge;

console.log(myAge);

// Multiplication
let score = 10;
score = 5 * score;
score *= 5;
console.log(score);

// Division
score /= 3;
console.log(score);

// Remainder
console.log(10 % 3);

// Exponent
console.log(Math.pow(7, 3));
console.log(9 ** 3);